def get_faq(faq_id: str) -> dict:
    """获取FAQ"""
    return KNOWLEDGE_BASE.get(faq_id.upper())


def search_faq(query: str) -> list:
    """搜索FAQ"""
    results = []
    query_lower = query.lower()
    for item in KNOWLEDGE_BASE.values():
        if item.get("category") in ["行李", "值机", "改签退票", "延误赔偿", "会员", "特殊服务"]:
            # 检查关键词匹配
            keywords = item.get("keywords", [])
            question = item.get("question", "").lower()
            answer = item.get("answer", "").lower()

            score = 0
            for keyword in keywords:
                if keyword in query_lower:
                    score += 10
            if query_lower in question:
                score += 5
            if query_lower in answer:
                score += 2

            if score > 0:
                results.append({"item": item, "score": score})

    return sorted(results, key=lambda x: x["score"], reverse=True)


def get_faqs_by_category(category: str) -> list:
    """按类别获取FAQ"""
    return [item for item in KNOWLEDGE_BASE.values() if item.get("category") == category]


