@redteam.command()
@click.option(
    "--preset",
    "-p",
    default="quick-scan",
    help="Preset to use (owasp:llm, mitre:atlas, quick-scan)",
)
@click.option(
    "--output",
    "-o",
    default="promptfoo.yaml",
    type=click.Path(),
    help="Output configuration file path",
)
@click.option(
    "--target",
    "-t",
    default="",
    help="Target URL or guardrail name",
)
@click.option(
    "--list-presets",
    is_flag=True,
    help="List available presets",
)
@click.option(
    "--list-plugins",
    is_flag=True,
    help="List available plugins",
)
def init(preset: str, output: str, target: str, list_presets: bool, list_plugins: bool):
    """Initialize a security test configuration.

    Creates a promptfoo-style YAML configuration file based on the selected preset.

    \b
    Examples:
        dspy-guardrails redteam init --preset owasp:llm -o security.yaml
        dspy-guardrails redteam init --preset quick-scan --target http://localhost:9000
        dspy-guardrails redteam init --list-presets
    """
    # List presets
    if list_presets:
        click.echo("Available presets:\n")
        for preset_id, info in list_available_presets().items():
            click.echo(f"  {preset_id}")
            click.echo(f"    {info['description']}")
            click.echo(f"    Plugins: {info['plugin_count']}, Strategies: {info['strategy_count']}")
            click.echo()
        return

    # List plugins
    if list_plugins:
        click.echo("Available plugins:\n")
        for plugin_id, info in list_available_plugins().items():
            owasp = f" (OWASP: {info['owasp']})" if info.get('owasp') else ""
            click.echo(f"  {plugin_id}{owasp}")
            click.echo(f"    {info['description']}")
            click.echo()
        return

    try:
        # Create configuration from preset
        config = PromptfooConfig.from_preset(preset)

        # Add target if provided
        if target:
            config.targets = [TargetConfig.from_dict(target)]

        # Save configuration
        output_path = Path(output)
        config.save(output_path)

        click.echo(f"Configuration created: {output_path}")
        click.echo(f"  Preset: {preset}")
        click.echo(f"  Plugins: {len(config.plugins)}")
        click.echo(f"  Strategies: {len(config.strategies)}")

        if config.targets:
            click.echo(f"  Target: {config.targets[0].to_target_string()}")

        click.echo(f"\nRun tests with: dspy-guardrails redteam run --config {output_path}")

    except ValueError as e:
        click.echo(f"Error: {e}", err=True)
        sys.exit(1)


@redteam.command()
@click.option(
    "--config",
    "-c",
    type=click.Path(exists=False),
    help="Configuration file path",
)
@click.option(
    "--preset",
    "-p",
    help="Use preset instead of config file",
)
@click.option(
    "--target",
    "-t",
    help="Target override (URL or guardrail:name)",
)
@click.option(
    "--output",
    "-o",
    default="./results",
    type=click.Path(),
    help="Output directory for results",
)
@click.option(
    "--format",
    "-f",
    "output_format",
    multiple=True,
    type=click.Choice(["console", "json", "html"]),
    default=["console"],
    help="Output format(s)",
)
@click.option(
    "--cache/--no-cache",
    default=True,
    help="Enable/disable LLM call caching",
)
@click.option(
    "--parallel/--no-parallel",
    default=True,
    help="Enable/disable parallel execution",
)
@click.option(
    "--workers",
    "-w",
    default=5,
    type=int,
    help="Number of parallel workers",
)
@click.option(
    "--threshold",
    default=None,
    type=float,
    help="Block rate threshold for pass/fail (0.0-1.0). Defaults to config.thresholds.block_rate or 0.8",
)
@click.option(
    "--strict/--no-strict",
    default=True,
    help="Fail fast on unknown plugins/strategies in config",
)
@click.option(
    "--verbose",
    "-v",
    is_flag=True,
    help="Verbose output",
)
def run(
    config: str | None,
    preset: str | None,
    target: str | None,
    output: str,
    output_format: tuple,
    cache: bool,
    parallel: bool,
    workers: int,
    threshold: float | None,
    strict: bool,
    verbose: bool,
):
    """Run security tests against a target.

    \b
    Examples:
        dspy-guardrails redteam run --config promptfoo.yaml
        dspy-guardrails redteam run --preset quick-scan --target http://localhost:9000
        dspy-guardrails redteam run -c security.yaml --threshold 0.9 -f json
    """
    # Load configuration
    try:
        if preset:
            test_config = PromptfooConfig.from_preset(preset, validate=strict)
        elif config:
            test_config = PromptfooConfig.from_yaml(config, validate=strict)
        else:
            click.echo("Error: Either --config or --preset is required", err=True)
            sys.exit(1)

        # Override settings
        if target:
            test_config.targets = [TargetConfig.from_dict(target)]

        test_config.parallel = parallel
        test_config.maxWorkers = workers
        test_config.cache.enabled = cache
        test_config.output.formats = list(output_format)
        test_config.output.dir = output
        if strict:
            test_config.validate()
        else:
            _coerce_non_strict_config(test_config)

    except FileNotFoundError as e:
        click.echo(f"Error: Configuration file not found: {e}", err=True)
        sys.exit(1)
    except ValueError as e:
        click.echo(f"Error: Invalid configuration: {e}", err=True)
        sys.exit(1)

    # Resolve effective thresholds.
    effective_block_threshold = (
        float(threshold)
        if threshold is not None
        else float(test_config.thresholds.get("block_rate", 0.8))
    )
    if not 0.0 <= effective_block_threshold <= 1.0:
        click.echo(
            f"Error: block rate threshold must be in [0.0, 1.0], got {effective_block_threshold}",
            err=True,
        )
        sys.exit(1)

    effective_critical_threshold = None
    if "critical_block_rate" in test_config.thresholds:
        effective_critical_threshold = float(test_config.thresholds["critical_block_rate"])
        if not 0.0 <= effective_critical_threshold <= 1.0:
            click.echo(
                "Error: critical_block_rate threshold must be in [0.0, 1.0], "
                f"got {effective_critical_threshold}",
                err=True,
            )
            sys.exit(1)

    # Validate targets
    if not test_config.targets:
        click.echo("Error: No target specified. Use --target or add targets to config.", err=True)
        sys.exit(1)
    if not test_config.plugins:
        click.echo("Error: No valid plugins configured after normalization.", err=True)
        sys.exit(1)

    # Create target
    target_config = test_config.targets[0]
    try:
        from ..testing.targets import GuardrailTarget, MockTarget

        if target_config.type == "mock":
            test_target = MockTarget()
        elif target_config.type == "guardrail":
            from ..guardrail import guardrail
            guardrail_fn = getattr(guardrail, target_config.url, None)
            if guardrail_fn is None:
                click.echo(f"Error: Guardrail '{target_config.url}' not found", err=True)
                sys.exit(1)
            test_target = GuardrailTarget(guardrail_fn=guardrail_fn)
        elif target_config.type == "http":
            test_target = _create_http_target(target_config)
        else:
            click.echo(
                f"Error: Unsupported target type '{target_config.type}'. "
                "Supported: http, guardrail, mock",
                err=True,
            )
            sys.exit(1)
    except Exception as e:
        click.echo(f"Error creating target: {e}", err=True)
        sys.exit(1)

    # Create cache
    llm_cache = None
    if test_config.cache.enabled:
        llm_cache = LLMCallCache(
            cache_dir=test_config.cache.dir,
            ttl_seconds=test_config.cache.ttl,
        )

    # Create runner
    runner = ConcurrentTestRunner(test_config, cache=llm_cache)

    # Progress callback
    def on_progress(progress: RunProgress):
        if verbose:
            click.echo(
                f"\rProgress: {progress.completed}/{progress.total} "
                f"({progress.percent_complete:.1f}%) "
                f"Block rate: {progress.block_rate:.1%} "
                f"[{progress.current_plugin}]",
                nl=False,
            )

    # Run tests
    click.echo("Running security tests...")
    click.echo(f"  Target: {target_config.to_target_string()}")
    click.echo(f"  Plugins: {len(test_config.plugins)}")
    click.echo(f"  Strategies: {len(test_config.strategies)}")
    click.echo(f"  Parallel: {test_config.parallel} (workers: {test_config.maxWorkers})")
    click.echo(f"  Cache: {'enabled' if test_config.cache.enabled else 'disabled'}")
    click.echo(f"  Block threshold: {effective_block_threshold:.1%}")
    if effective_critical_threshold is not None:
        click.echo(f"  Critical threshold: {effective_critical_threshold:.1%}")
    click.echo()

    try:
        summary = runner.run(test_target, on_progress=on_progress if verbose else None)
    except Exception as e:
        click.echo(f"\nError during test execution: {e}", err=True)
        if verbose:
            import traceback
            click.echo(traceback.format_exc(), err=True)
        sys.exit(2)

    # Clear progress line
    if verbose:
        click.echo()

    # Display results
    click.echo("\n" + "=" * 60)
    click.echo("SECURITY TEST RESULTS")
    click.echo("=" * 60)

    click.echo(f"\nTotal Tests: {summary.total_tests}")
    click.echo(f"Blocked Attacks: {summary.blocked_attacks}")
    click.echo(f"Successful Attacks: {summary.successful_attacks}")
    click.echo(f"Errors: {summary.errors}")
    click.echo(f"\nBlock Rate: {summary.block_rate:.1%}")
    click.echo(f"Attack Success Rate: {summary.attack_success_rate:.1%}")
    click.echo(f"Execution Time: {summary.total_time_seconds:.2f}s")

    if test_config.cache.enabled:
        click.echo(f"\nCache Hit Rate: {summary.cache_hit_rate:.1%}")
        click.echo(f"Cost Saved: ${summary.cost_saved:.4f}")

    # Display vulnerabilities
    if summary.vulnerabilities:
        click.echo(f"\n{'!' * 60}")
        click.echo(f"VULNERABILITIES FOUND: {len(summary.vulnerabilities)}")
        click.echo(f"{'!' * 60}")

        for vuln in summary.vulnerabilities[:10]:
            severity = vuln.payload.severity.value if hasattr(vuln.payload.severity, 'value') else str(vuln.payload.severity)
            click.echo(f"\n  [{severity.upper()}] {vuln.payload.id}")
            click.echo(f"    Technique: {vuln.payload.technique}")
            prompt_preview = vuln.payload.prompt[:80] + "..." if len(vuln.payload.prompt) > 80 else vuln.payload.prompt
            click.echo(f"    Payload: {prompt_preview}")

        if len(summary.vulnerabilities) > 10:
            click.echo(f"\n  ... and {len(summary.vulnerabilities) - 10} more")

    # Critical-only block rate (if critical payloads exist).
    critical_results = [
        r
        for r in summary.results
        if (
            r.payload.severity.value
            if hasattr(r.payload.severity, "value")
            else str(r.payload.severity)
        ).lower()
        == "critical"
    ]
    critical_total = len(critical_results)
    critical_blocked = sum(1 for r in critical_results if r.blocked)
    critical_block_rate = (
        critical_blocked / critical_total if critical_total > 0 else 1.0
    )
    if critical_total > 0:
        click.echo(
            f"\nCritical Block Rate: {critical_block_rate:.1%} "
            f"({critical_blocked}/{critical_total})"
        )

    # Save results
    output_dir = Path(output)
    output_dir.mkdir(parents=True, exist_ok=True)
    result_payload = summary.to_dict()
    result_payload["thresholds"] = test_config.thresholds
    result_payload["effective_thresholds"] = {
        "block_rate": effective_block_threshold,
        **(
            {"critical_block_rate": effective_critical_threshold}
            if effective_critical_threshold is not None
            else {}
        ),
    }
    result_payload["critical_block_rate"] = critical_block_rate
    result_payload["critical_totals"] = {
        "total": critical_total,
        "blocked": critical_blocked,
    }

    if "json" in output_format:
        json_path = output_dir / "results.json"
        with open(json_path, "w", encoding="utf-8") as f:
            json.dump(result_payload, f, indent=2)
        click.echo(f"\nJSON results saved to: {json_path}")

    if "html" in output_format:
        html_path = output_dir / "results.html"
        _save_html_report(summary, html_path)
        click.echo(f"HTML report saved to: {html_path}")

    # Check threshold
    passed = summary.block_rate >= effective_block_threshold
    threshold_messages = [
        "block rate "
        f"{summary.block_rate:.1%} "
        f"{'>=' if summary.block_rate >= effective_block_threshold else '<'} "
        f"{effective_block_threshold:.1%}"
    ]
    if effective_critical_threshold is not None:
        critical_pass = critical_block_rate >= effective_critical_threshold
        passed = passed and critical_pass
        threshold_messages.append(
            "critical block rate "
            f"{critical_block_rate:.1%} "
            f"{'>=' if critical_pass else '<'} "
            f"{effective_critical_threshold:.1%}"
        )

    click.echo(f"\n{'=' * 60}")
    if passed:
        click.echo(f"PASSED - {'; '.join(threshold_messages)}")
        sys.exit(0)
    else:
        click.echo(f"FAILED - {'; '.join(threshold_messages)}")
        sys.exit(1)


