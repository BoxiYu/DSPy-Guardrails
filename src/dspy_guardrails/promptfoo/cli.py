@ci.command()
@click.option(
    "--provider",
    "-p",
    type=click.Choice(["github", "gitlab", "jenkins"]),
    default="github",
    help="CI provider",
)
@click.option(
    "--output",
    "-o",
    type=click.Path(),
    help="Output path (default: auto-detect)",
)
@click.option(
    "--config",
    "-c",
    default="promptfoo.yaml",
    help="Path to promptfoo config file to use",
)
@click.option(
    "--threshold",
    default=0.8,
    type=float,
    help="Block rate threshold",
)
def generate(provider: str, output: str | None, config: str, threshold: float):
    """Generate CI/CD workflow configuration.

    \b
    Examples:
        dspy-guardrails ci generate --provider github
        dspy-guardrails ci generate -p gitlab -o .gitlab-ci.yml
    """
    from ..testing.ci import generate_workflow

    try:
        workflow = generate_workflow(
            provider=provider,
            config_path=config,
            threshold=threshold,
        )

        # Determine output path
        if output is None:
            if provider == "github":
                output = ".github/workflows/security-scan.yml"
            elif provider == "gitlab":
                output = ".gitlab-ci.yml"
            elif provider == "jenkins":
                output = "Jenkinsfile"

        # Create directories
        output_path = Path(output)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        # Write workflow
        with open(output_path, "w") as f:
            f.write(workflow)

        click.echo(f"CI workflow generated: {output_path}")
        click.echo(f"  Provider: {provider}")
        click.echo(f"  Config: {config}")
        click.echo(f"  Threshold: {threshold:.0%}")

    except Exception as e:
        click.echo(f"Error generating workflow: {e}", err=True)
        sys.exit(1)


@ci.command()
@click.option(
    "--results",
    "-r",
    type=click.Path(exists=True),
    required=True,
    help="Path to results JSON file",
)
@click.option(
    "--threshold",
    default=0.8,
    type=float,
    help="Block rate threshold (0.0-1.0)",
)
@click.option(
    "--format",
    "-f",
    type=click.Choice(["text", "json", "github"]),
    default="text",
    help="Output format",
)
def check(results: str, threshold: float, format: str):
    """Check test results against threshold.

    Exits with code 0 if passed, 1 if failed.

    \b
    Examples:
        dspy-guardrails ci check --results results.json --threshold 0.8
        dspy-guardrails ci check -r results.json -f github
    """
    try:
        with open(results) as f:
            data = json.load(f)

        # Extract block rate
        block_rate_str = data.get("block_rate", "0%")
        if isinstance(block_rate_str, str):
            block_rate = float(block_rate_str.rstrip("%")) / 100
        else:
            block_rate = float(block_rate_str)

        passed = block_rate >= threshold

        if format == "json":
            output = {
                "passed": passed,
                "block_rate": block_rate,
                "threshold": threshold,
                "vulnerabilities": len(data.get("vulnerabilities", [])),
            }
            click.echo(json.dumps(output, indent=2))

        elif format == "github":
            # GitHub Actions output format
            click.echo(f"::set-output name=passed::{str(passed).lower()}")
            click.echo(f"::set-output name=block_rate::{block_rate:.2%}")
            if not passed:
                click.echo(f"::error::Security check failed. Block rate {block_rate:.1%} < {threshold:.1%}")

        else:
            click.echo(f"Block Rate: {block_rate:.1%}")
            click.echo(f"Threshold: {threshold:.1%}")
            click.echo(f"Status: {'PASSED' if passed else 'FAILED'}")

            if data.get("vulnerabilities"):
                click.echo(f"Vulnerabilities: {len(data['vulnerabilities'])}")

        sys.exit(0 if passed else 1)

    except FileNotFoundError:
        click.echo(f"Error: Results file not found: {results}", err=True)
        sys.exit(2)
    except json.JSONDecodeError:
        click.echo("Error: Invalid JSON in results file", err=True)
        sys.exit(2)
    except Exception as e:
        click.echo(f"Error: {e}", err=True)
        sys.exit(2)


